    def has_wall_at(self, x, y):
        return self.grid[int(y // TILESIZE)][int(x // TILESIZE)]

    def render(self, screen):
        for i in range(len(self.grid)):
            for j in range(len(self.grid[0])):

                value = self.grid[i][j]
                tile_x = j * TILESIZE
                tile_y = i * TILESIZE

                if value == 0:
                    pygame.draw.rect(screen, WHITE, (tile_x, tile_y, TILESIZE-1, TILESIZE-1))
                elif value == 1:
                    pygame.draw.rect(screen, DARK_GREY, (tile_x, tile_y, TILESIZE-1, TILESIZE-1))

